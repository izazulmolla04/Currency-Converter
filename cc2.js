const fromSelect = document.querySelector("select[name='from']");
const toSelect = document.querySelector("select[name='to']");
const amountInput = document.getElementById("amount");
const resultInput = document.getElementById("convertedAmount");
const convertBtn = document.getElementById("convertBtn");

// Populate dropdowns
for (let currency in countryList) {
  let option1 = document.createElement("option");
  option1.value = currency;
  option1.text = currency;
  if (currency === "USD") option1.selected = true;
  fromSelect.appendChild(option1);

  let option2 = document.createElement("option");
  option2.value = currency;
  option2.text = currency;
  if (currency === "INR") option2.selected = true;
  toSelect.appendChild(option2);
}

// Update flag when dropdown changes
function loadflag(selectEl, flagId) {
  let currencyCode = selectEl.value;
  let countryCode = countryList[currencyCode];
  document.getElementById(flagId).src = `https://flagsapi.com/${countryCode}/flat/64.png`;
}

// Convert currency
convertBtn.addEventListener("click", async () => {
  let amountVal = amountInput.value.trim();
  if (amountVal === "" || isNaN(amountVal) || amountVal <= 0) {
    alert("Please enter a valid amount");
    return;
  }

  let fromCurrency = fromSelect.value;
  let toCurrency = toSelect.value;
  const url = `https://open.er-api.com/v6/latest/${fromCurrency}`;
  let response = await fetch(url);
  let data = await response.json();
   if (data.result === "success" && data.rates[toCurrency]) {
  let rate = data.rates[toCurrency];
  resultInput.value = (amountVal * rate).toFixed(2);
} else {
  alert("Conversion failed â€” check currency codes or try again later.");
}
});


// Load default flags on page load
window.addEventListener("load", () => {
  loadflag(fromSelect, "fromFlag");
  loadflag(toSelect, "toFlag");
});